func resourceProjectUserAttributeDelete(ctx context.Context, d *schema.ResourceData, m interface{}) diag.Diagnostics {
	var diags diag.Diagnostics
	c := m.(client.OktaPAMClient)

	projectName := d.Get(attributes.ProjectName).(string)
	userName := d.Get(attributes.UserName).(string)
	attrId := d.Id()

	err := c.DeleteProjectUserAttribute(ctx, projectName, userName, attrId)
	if err != nil {
		return diag.FromErr(err)
	}

	d.SetId("")
	return diags
}

